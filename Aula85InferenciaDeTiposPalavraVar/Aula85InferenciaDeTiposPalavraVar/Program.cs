using System;

namespace Aula85InferenciaDeTiposPalavraVar
{
    class Program
    {
        static void Main(string[] args)
        {
            var x = 10; // inteiro
            var y = 2.5; // double
            var z = 4.5f; // float
            var a = "Maria"; // string

            Console.WriteLine(x);
            Console.WriteLine(y);
            Console.WriteLine(z);
            Console.WriteLine(a);
        }
    }
}
